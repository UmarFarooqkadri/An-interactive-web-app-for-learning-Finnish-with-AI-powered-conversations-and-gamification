// Helper function to get all vocabulary words
export const getAllVocabulary = () => {
  const allWords = [];
  Object.keys(VOCABULARY_CATEGORIES).forEach(categoryKey => {
    const category = VOCABULARY_CATEGORIES[categoryKey];
    category.words.forEach((word, index) => {
      allWords.push({
        id: `${categoryKey}-${index}`,
        category: category.name,
        categoryKey: categoryKey,
        ...word
      });
    });
  });
  return allWords;
};

// Helper function to get words by category
export const getWordsByCategory = (categoryKey) => {
  const category = VOCABULARY_CATEGORIES[categoryKey];
  if (!category) return [];

  return category.words.map((word, index) => ({
    id: `${categoryKey}-${index}`,
    category: category.name,
    categoryKey: categoryKey,
    ...word
  }));
};
